package javaProgram;

public class Operator_Questions {
    public static void main(String[] args) {
        int x = 4, y=3, z=-4;
        int result;
        result = x + y;
        System.out.println(result);
        result = x & y;
        System.out.println(result);
        result = x | y;
        System.out.println(result);
        result = ~y;
        System.out.println(result);
        result = ~z;
        System.out.println(result);
        result = x ^ y;
        System.out.println(result);

    }
}
