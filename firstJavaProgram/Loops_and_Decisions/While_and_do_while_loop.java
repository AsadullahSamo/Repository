package Loops_and_Decisions;

public class While_and_do_while_loop {
    public static void main(String[] args) {
        // While loop
        System.out.println("While loop");
        int i=1;
        while(i<=3)
        {
            System.out.println(i);
            i++;
        }
        // do-while loop
        System.out.println("do-While loop");
        int a=1;
        do {
            System.out.println(a);
            a++;
        }while(a<4);
    }
}
